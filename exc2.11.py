# Escreva um programa que verifique se uma temperatura está acima, abaixo ou
# dentro da faixa normal (36°C a 37°C).
temperatura = 39

if temperatura > 37:
    print (f"A temperatura está acima da faixa normal")
elif temperatura < 36:
    print (f"A temperatura está abaixo da faixa normal")
else:
    print (f"A temperatura está dentro da faixa normal")